num_str = "0123456789"

print(num_str[2:6])
# 截取从2到5
print(num_str[2:])
# 截取2和之后的
print(num_str[:6])
# 截取5和之前的
print(num_str[2:-1])
# 从2截取直到倒数第二个
print(num_str[-2:])
# 从倒数第二个截取到末尾
print(num_str[::2])
# 隔一个取一个
print(num_str[::-1])
# 倒序截取
print(num_str[-2::-2])
# 从倒数第二个，隔一个取一个，截取到头
